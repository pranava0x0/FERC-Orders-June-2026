/* Build the RM26-4 comment audit trail from the saved pieces, and aggregate categorization from the
 * per-comment summaries. Re-runnable as more files/summaries land.
 *
 *   website (data.js)  ->  rm26-4-comments.json (manifest + buckets)
 *                      ->  rm26-4-files.json (per-accession downloaded files + attachments)
 *                      ->  summaries/<accession>.json (per-comment structured summary)
 *                      ->  rm26-4-categorization.json (stances aggregated across summaries)
 *
 * Outputs: sources/comments/rm26-4-audit-index.json  (the chain, one row per comment)
 *          sources/comments/rm26-4-categorization.json (stance tallies per reform category + bucket)
 * Run: node tools/build-comment-audit.mjs
 */
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
const hasBody = (acc) => { const d = join(here, "..", "sources", "comments", "files", acc); return existsSync(d) && readdirSync(d).some((f) => /\.(pdf|docx?|txt)$/i.test(f)); };
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const C = join(here, "..", "sources", "comments");
const read = (p, d) => (existsSync(p) ? JSON.parse(readFileSync(p, "utf8")) : d);

const comments = read(join(C, "rm26-4-comments.json"), { comments: [] }).comments;
const filesMeta = read(join(C, "rm26-4-files.json"), { files: {} }).files; // acc -> [{name,type,size,security}]
const sumDir = join(C, "summaries");
const summaries = !existsSync(sumDir) ? [] : readdirSync(sumDir).filter((f) => f.endsWith(".json"))
  .map((f) => JSON.parse(readFileSync(join(sumDir, f), "utf8")));
const sumByAcc = Object.fromEntries(summaries.map((s) => [s.accession, s]));

const CATS = ["study", "cost", "colo", "flex", "proximate"];
const CAT_LABEL = { study: "Study process + alt-tech", cost: "Cost-shifting + transparency", colo: "Co-location / BTM", flex: "Flexible-load services", proximate: "Proximate-generation interconnection" };
const norm = (v) => (!v ? "silent" : /silent/i.test(v) ? "silent" : /oppose/i.test(v) ? "oppose" : /mixed|partial/i.test(v) ? "mixed" : /support|favor|yes/i.test(v) ? "support" : "other");
const BUCKET_LABELS = read(join(C, "rm26-4-comments.json"), { bucket_labels: {} }).bucket_labels || {};

// audit index: one row per comment, threading the chain
const index = comments.map((c) => {
  const s = sumByAcc[c.acc];
  return {
    accession: c.acc,
    filer: c.org,
    bucket: c.bucket,
    filed: c.filed,
    elibrary: `https://elibrary.ferc.gov/eLibrary/filelist?accession_Number=${c.acc}`,
    files: filesMeta[c.acc] || (s && s.files ? s.files.map((f) => ({ name: f.name, type: f.type })) : []),
    summary: s ? `sources/comments/summaries/${c.acc}.json` : null,
    summarized: !!s,
    inventoried: !!filesMeta[c.acc],
    downloaded: hasBody(c.acc),
  };
});

// categorization aggregated from the summaries' per-category stances
const byCategory = Object.fromEntries(CATS.map((k) => [k, { support: 0, oppose: 0, mixed: 0, silent: 0, other: 0 }]));
const byBucketCat = {};
for (const s of summaries) {
  for (const k of CATS) {
    const st = norm(s.stances && s.stances[k]);
    byCategory[k][st] = (byCategory[k][st] || 0) + 1;
    (byBucketCat[s.bucket] = byBucketCat[s.bucket] || {});
    (byBucketCat[s.bucket][k] = byBucketCat[s.bucket][k] || {});
    byBucketCat[s.bucket][k][st] = (byBucketCat[s.bucket][k][st] || 0) + 1;
  }
}

writeFileSync(join(C, "rm26-4-audit-index.json"), JSON.stringify({
  captured_at: "2026-06-24",
  note: "Audit chain per comment: eLibrary accession -> downloaded files/attachments -> per-comment summary -> bucket. Generated by tools/build-comment-audit.mjs.",
  total_comments: comments.length,
  summarized: summaries.length,
  with_files: index.filter((r) => r.downloaded).length,
  index,
}, null, 2));

writeFileSync(join(C, "rm26-4-categorization.json"), JSON.stringify({
  captured_at: "2026-06-24",
  note: "Stances per reform category, aggregated across the per-comment summaries (not the whole 273 — only summarized filings). support/oppose/mixed/silent.",
  category_labels: CAT_LABEL,
  summarized: summaries.length,
  by_category: byCategory,
  by_bucket_category: byBucketCat,
}, null, 2));

// site-ready flagship digest (paste into data.js comments.flagships)
const flagships = summaries
  .sort((a, b) => (a.filed < b.filed ? -1 : 1))
  .map((s) => ({
    acc: s.accession,
    filer: s.filer,
    bucket: s.bucket,
    bucketLabel: BUCKET_LABELS[s.bucket] || s.bucket,
    summary: s.summary,
    quote: s.quotes && s.quotes[0] ? s.quotes[0].text : null,
    stances: s.stances,
    file: s.files && s.files[0] ? s.files[0].name : null,
    elibrary: s.elibrary,
  }));
writeFileSync(join(C, "rm26-4-flagships-digest.json"), JSON.stringify(flagships, null, 2));

console.log(`comments=${comments.length} summarized=${summaries.length} with_files=${index.filter((r) => r.downloaded).length}`);
console.log("stances by reform category (across summaries):");
for (const k of CATS) console.log(`  ${CAT_LABEL[k]}: ${JSON.stringify(byCategory[k])}`);
